.MODEL SAMALL 
.STACK 100H
.DATA  
MSG DB '0 $'
MSG2 DB 'E $'
.CODE
MAIN PROC 
    
MOV AX,@DATA
MOV DS,AX
   
MOV AH,1
INT 21H 
MOV BL,AL

MOV AH,2
MOV DL,0DH
INT 21H
MOV DL ,0AH
INT 21H

CMP BL,'1'  
JE NEXT 
CMP BL, '3'
JE NEXT 

CMP BL,'2'
JE NEXT2
CMP BL,'4'
JE NEXT2


NEXT:  

MOV AH,9
LEA DX,MSG
INT 21H
JMP FINISH
  
NEXT2:
MOV AH,9
LEA DX,MSG2
INT 21H
   
   
   
 

FINISH:    
MOV AH,4CH
INT 21H
MAIN ENDP 
END MAIN