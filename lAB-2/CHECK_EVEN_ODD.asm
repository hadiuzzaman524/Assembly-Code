.MODEL SAMALL 
.STACK 100H
.DATA  
MSG DB 'EVEN$'
MSG2 DB 'ODD $'
.CODE
MAIN PROC 
    
MOV AX,@DATA
MOV DS,AX
         
MOV AH,1
INT 21H
MOV BL,AL


CMP BL,'1'
JE ODD 
CMP BL,'3'
JE ODD
CMP BL,'5'
JE ODD
CMP BL,'7'
JE ODD
CMP BL,'9'
JE ODD 
CMP BL,'0'
JE EVEN  
CMP BL,'2'
JE EVEN
CMP BL,'4'
JE EVEN
CMP BL,'6'
JE EVEN
CMP BL,'8'
JE EVEN
  
    
ODD:  
 MOV AH,9
 LEA DX,MSG2
 INT 21H
 JMP FINISH
  


EVEN:  
 MOV AH,9
 LEA DX,MSG
 INT 21H
JMP FINISH


FINISH:    
MOV AH,4CH
INT 21H
MAIN ENDP 
END MAIN