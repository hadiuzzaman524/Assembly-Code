.MODEL SMALL 
.STACK 100H
.DATA 
.CODE 
MAIN PROC 
    
XOR BX,BX 
MOV AH,1 
INT 21H 

TOP: 
CMP AL,0DH 
JE END_TOP
AND AL,0FH 
SHL BX,1 
OR BL,AL 
INT 21H 
JMP TOP  




END_TOP: 

MOV AH,2  
MOV DL,0DH 
INT 21H 
MOV DL,0AH 
INT 21H 

MOV CX,16
MOV AH,2

 
OUTPUT: 
CMP CX,0
JE SKIP 
ROL BX,1 
JNC NEXT 
MOV DL,'1'
INT 21H 
DEC CX 
JMP OUTPUT

NEXT: 
MOV DL,'0'
INT 21H 
DEC CX 
JMP OUTPUT
 

SKIP:     
MOV AH,4CH 
INT 21H 
MAIN ENDP 
END MAIN 
     