.MODEL SMALL 
.STACK 100H
.DATA 
.CODE 
MAIN PROC 
    
XOR BX,BX 
MOV AH,1 
INT 21H 

TOP: 
CMP AL,0DH 
JE END_TOP 

CMP AL,39H
JG LETTER
AND AL,0FH 
JMP SHIFT 

LETTER: 
SUB AL,37H

SHIFT: 
SHL BX,4
OR BL,AL 
INT 21H 
JMP TOP  




END_TOP: 

MOV AH,2  
MOV DL,0DH 
INT 21H 
MOV DL,0AH 
INT 21H 

MOV CX,4
MOV AH,2

 
OUTPUT: 
CMP CX,0
JE SKIP 



MOV DX,BX
SAR BX,4  

CMP DL,10 
JL DIGIT_CONVERT 
JGE LETTER_CONVERT

DIGIT_CONVERT: 
ADD DL,30H 
INT 21H 
DEC CX
JMP OUTPUT

LETTER_CONVERT:
ADD DL,55
INT 21H 
DEC CX
JMP OUTPUT 


 

SKIP:     
MOV AH,4CH 
INT 21H 
MAIN ENDP 
END MAIN 
     