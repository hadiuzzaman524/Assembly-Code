.MODEL SMALL 
.STACK 100H
.DATA 
.CODE 

MAIN PROC 

MOV AH,1
INT 21H 
MOV CL,AL 

MOV AH,1 
INT 21H 
MOV BL,AL 

MOV AH,1 
INT 21H 
MOV BH,AL 
            
TOP:
 CMP CL,BL 
 JGE GRATER
 
 GRATER: 
 MOV BL,CL 
 CMP BL,BH 
 JGE GR  
 CMP BL,BH
 JL PRINT
 
 GR:
 MOV BH,BL 

 
    
PRINT:
   MOV AH,2
   MOV DL ,0DH
   INT 21H
   MOV DL,0AH
   INT 21H  
   
   
   MOV AH,2
   MOV DL,BH
   INT 21H 
  

END:
MOV AH,4CH 
INT 21H 
MAIN ENDP 
END MAIN 